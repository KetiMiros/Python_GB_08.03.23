#     (('Й', 'Ы'), 4),
#     (('Ж', 'З', 'Х', 'Ц', 'Ч'), 5),
#     (('Ш', 'Э', 'Ю'), 8),
#     (('Ф', 'Щ', 'Ъ'), 10),]


total_result = 0
letter = input("Введите слово на английском языке: ")
for char in letter:
    for template, result in let_eng:
        if char.upper() in template:
            total_result += result

print(total_result)
